package bean;

public class Reservation {
	
	private String ac_reservationtime;
	private String ac_st_num;
	private String ac_acceptId;
	private String ac_requestId;
	private int ac_eat;
	
	public String getAc_reservationtime() {
		return ac_reservationtime;
	}
	public void setAc_reservationtime(String ac_reservationtime) {
		this.ac_reservationtime = ac_reservationtime;
	}
	public String getAc_st_num() {
		return ac_st_num;
	}
	public void setAc_st_num(String ac_st_num) {
		this.ac_st_num = ac_st_num;
	}
	public String getAc_acceptId() {
		return ac_acceptId;
	}
	public void setAc_acceptId(String ac_acceptId) {
		this.ac_acceptId = ac_acceptId;
	}
	public String getAc_requestId() {
		return ac_requestId;
	}
	public void setAc_requestId(String ac_requestId) {
		this.ac_requestId = ac_requestId;
	}
	public int getAc_eat() {
		return ac_eat;
	}
	public void setAc_eat(int ac_eat) {
		this.ac_eat = ac_eat;
	}
	
	
	

}
